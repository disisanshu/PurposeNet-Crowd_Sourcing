<span style="color:Red;font-size:22px">Features of Artifact</span><br><br>
<p style="font:20px Droid Sans;color:black;margin-left:20px" >
Every artifact is made up of some core-components and some Associate component.<br><br>
<span style="color:Green"><u>Core-Component</u></span>: Essential parts of an artifact without which both the make and the functionality of the artifact is not fulfilled. <br>
Example: Nib of a pen, body of a car.<br><br>
<span style="color:Green"><u>Associate Component</u></span>: Constituent part of an artifact but not essential part for the making of the artifact. Such artifacts can be of two kinds:<br>
<ul style="margin-left:40px">
<li style="font:20px Droid Sans;color:black;">Plays a role to fulfill the primary purpose of the artifact.<br>
Example: Rear view window of car: Not essential for making of the car, but required for driving the car backward. Driving is a sub-action of transport, which is primary purpose action of car<br></li>
<li style="font:20px Droid Sans;color:black;"> Does not play a role to fulfill the primary purpose of the artifact<br></li>
</ul>
<p style="font:20px Droid Sans;color:black;margin-left:20px">
Example: Music system in a car<br><br>
<span style="color:Green"><u>External Accessory</u></span>: Artifacts that are not physically attached to the artifact under consideration; but without which the functionality of the artifact is not fulfilled.<br><br>
</p>
<span style="color:Red;font-size:22px;">Action and Sub-action</span><br><br>
<p style="font:20px Droid Sans;color:black;margin-left:20px">
Every artifact serves one primary action which we call its purpose. <br>
For example, purpose of the artifact 'car' is to transport things from one place to another. <br>
In the process of the action, the artifact may require to perform a set of sub-actions. <br>
For example, transporting by car involves <br>
<ul style="margin-left:40px">
<li style="font:20px Droid Sans;color:black;"> Put things inside the car </li>
<li style="font:20px Droid Sans;color:black;"> Drive the car from source place to destination </li>
<li style="font:20px Droid Sans;color:black;"> Alight the things transported from the car </li>
</ul>
</p>
<p style="font:20px Droid Sans;color:black;margin-left:20px" >
Thus, sub-actions of transporting by car include Putting things, Driving car and Alighting things.</p>
