	<ul>
	<li><b>Core-components:</b> If you think that the object B is absolutely necessary for the definition of A. nib is a core component of a pen.<br><br></li>
	<li><b>Purpose-Serving Accessory:</b> If you think that the object B is not necessary, but if present, it aids A in satisfying its primary purpose. ink gel is a purpose serving accessory for a pen.<br><br></li>
	<li><b>Non-Purpose Serving Accessory:</b> If you think that object B is often present in/with object A but does some other work other than satisfy its primary purpose. Cap of a pen is there to protect the nib and prevent ink from spilling, but does not aid a pen in writing. Hence it is a non-purpose serving accessory for a pen.<br><br></li>
	<li><b>Naccessory (necessary but Not an accessory):</b> If there is something without which object A cannot satisfy its purpose but object A is fully defined without object B. eg a pen is fully defined without ink but it cannot write if ink is not present, hence ink is a naccessory for a pen.<br><br></li>
	<li><b>Subtype:</b> if object B follows an "is-a" relationship with object A. eg. fountain-pen is a subtype of pen.<br><br></li>
